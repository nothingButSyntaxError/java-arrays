public class tabular {
    public static void main(String args[]) {
        int arr0[] = { 1, 2, 3 };
        String arr1[] = { "Meet", "Parth", "Yash" };
        String arr2[] = { "Python", "Javascript", "Rust" };
        for (int i = 0; i < arr0.length; i++) {
            System.out.println(arr0[i] + "\t" + arr1[i] + "\t" + arr2[i]);
        }
    }
}
